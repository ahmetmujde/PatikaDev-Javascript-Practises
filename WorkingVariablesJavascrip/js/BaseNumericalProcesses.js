
let  x = 3;
let  y = 6;
let  z = 0;
// add process
z = x + y
console.log(x, y)
console.log("x + y = " + z)
y += 5
x++
z += 1
console.log(x, y, z)

//subtraction process
z = y - x
console.log(x, y)
console.log("x + y = " + z)
y -= 2
x--
z -= 2 
console.log(x, y, z)

//multiplication process
z = y * x
console.log(x, y)
console.log("x * y = " + z)
y = y * 2
z *= 2 
console.log(x, y, z)

//divide process
z = y / x
console.log(x, y)
console.log("x / y = " + z)
y = y / 2
x /= 2 
